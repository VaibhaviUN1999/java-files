class SwitchDemo{
  public static void main(String[] args){
     int choice=300;
	 switch(choice){
	  case 1 : System.out.println("In choice 1");
	           break;
			 
	  case 2 : System.out.println("In choice 2");
	           break;	

	  case 3 : System.out.println("In choice 3");
	           break;		 

	  default : System.out.println("Invalid");	   
	 }
  }
}