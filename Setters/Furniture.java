class Furniture{
   static String name;
   static int price;
   static String color;
   static void setname(String n){
      name=n;
	  
   }
   static String getname(){
	   return name;
   }
   static void setprice(int p){
	   price=p;
   }
   static int getprice(){
	   return price;
   }
   static void setcolor(String c){
	   color=c;
   }
   static String getcolor(){
	   return color;
   }
}